package task2;

//4.1 + 15 * 7 + (28 / 5) ^ 2

public class CalculatorWithOperator {

    public static float operatorSum(float number1, float number2){
        return number1 + number2;
    }

    public static float operatorSubtraction(float number1, float number2){
        return number1 - number2;
    }

    public static float operatorMultiplication(float number1, float number2){
        return number1 * number2;
    }

    public static float operatorDivision(float number1, float number2){
        return number1 / number2;
    }



}
